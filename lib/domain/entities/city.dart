class City {
  final String name;
  final String country;
  final String region;
  final double lat;
  final double lon;
  final int? population;
  final String type;

  City({
    required this.name,
    required this.country,
    required this.region,
    required this.lat,
    required this.lon,
    required this.population,
    required this.type
  });
}
